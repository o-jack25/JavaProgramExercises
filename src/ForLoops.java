public class ForLoops {

    public static void main(String[] args){
        //executes a block of code a limited amount of times

        for(int i = 0; i <= 10; i++){
            System.out.println(i);
        }
        for(int i = 10; i >= 0; i--){
            System.out.println(i);
        }
        for(int i = 0; i <= 10; i += 2){
            System.out.println(i);
        }
    }

}
